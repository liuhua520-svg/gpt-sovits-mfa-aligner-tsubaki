export const detectLocaleFromBrowser = (languageTag: string): AppLocale => {
  const normalized = languageTag.toLowerCase()

  if (normalized.startsWith('zh')) {
    if (normalized.includes('hant') || normalized.includes('tw') || normalized.includes('hk') || normalized.includes('mo')) {
      return 'zh-TW'
    }
    return 'zh-CN'
  }

  if (normalized.startsWith('ja')) return 'ja'
  if (normalized.startsWith('ko')) return 'ko'
  return 'en'
}

const storedLocale = (() => {
  const saved = localStorage.getItem('app-locale') as AppLocale | null
  if (saved && (SUPPORTED_LOCALES as readonly string[]).includes(saved)) return saved
  return detectLocaleFromBrowser(navigator.language)
})()

export const i18n = createI18n({
  legacy: false,
  locale: storedLocale,
  fallbackLocale: 'en',
  messages,
})

export const elementPlusLocales = {
  'zh-CN': zhCn,
  'zh-TW': zhTw,
  en,
  ja,
  ko,
} as const

export const getElementPlusLocale = (locale: string) =>
  elementPlusLocales[(SUPPORTED_LOCALES.includes(locale as AppLocale) ? locale : 'en') as AppLocale]

export const useAppLocale = () => {
  const currentLocale = computed(() => i18n.global.locale.value)
  const setLocale = (locale: AppLocale) => {
    i18n.global.locale.value = locale
    localStorage.setItem('app-locale', locale)
  }

  return { currentLocale, setLocale, t: i18n.global.t }
}
